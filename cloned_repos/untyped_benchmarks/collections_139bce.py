def distinct(iterable, key=None):

    def _key(__i):
        return __i
    if key is not None:
        _key = cast(Callable[[Any], Hashable], key)
    seen = set()
    for item in iterable:
        if _key(item) in seen:
            continue
        seen.add(_key(item))
        yield item

